#ifndef __NODE_H_
#define __NODE_H_
//\=================================================================================
//\	Includes
//\=================================================================================
#include "UGFW++.h"
#include <vector>
class Node
{
protected:
	unsigned sprite_ = 0;
	Node* parent_ = nullptr;
	std::vector<Node*> children_;

public:
	Node();
	~Node();

	//\=================================================================================
	//\ Returns the nodes parent
	//\=================================================================================
	Node* GetParent() const;

	//\=================================================================================
	//\ Set the nodes parent and returns false if there is already a parent
	//\=================================================================================
	bool SetParent(Node* a_parent);

	//\=================================================================================
	//\ Add a child node to this node
	//\=================================================================================
	void AddChild(Node* a_child);

	//\=================================================================================
	//\ Get a child from this node
	//\=================================================================================
	Node* GetChild(unsigned a_num);

	//\=================================================================================
	//\ Start drawing this node and it's children
	//\=================================================================================
	void StartDrawing();

	//\=================================================================================
	//\ Stop drawing this node and it's children
	//\=================================================================================
	void StopDrawing();
};

#endif //__NODE_H_